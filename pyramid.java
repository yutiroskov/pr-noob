public class pyramid 
{
    public static void main(String[] args)
    {
        System.out.println("pyramid printing pattern");
        int i,j;
        for(i=0;i<=14;i++)
        {
            for(j=0;j<=i;j++)
            {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
